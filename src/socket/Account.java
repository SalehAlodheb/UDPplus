
package socket;



public class Account {
    
    
  private int number;
  private String name;

    public Account(int id, String name) {
        this.number = id;
        this.name = name;
    }

    public int getId() {
        return number;
    }

    public void setId(int id) {
        this.number = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
  
  
  


  
    
}
